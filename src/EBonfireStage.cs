namespace Bonfires
{
    // Enum to represent the distinct stages of a bonfire, improving code readability.
    public enum EBonfireStage
    {
        Extinct,    // 0: Burned out, needs to be rebuilt
        Base,       // 1: The initial base structure
        Construct1, // 2: First stage of construction
        Construct2, // 3: Second stage of construction
        Construct3  // 4: Fully constructed, ready for fuel
    }
}
